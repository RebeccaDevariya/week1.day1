package week1.day1;

public class Factorial {
	public static void main(String args[]){  
		  int i;  
		  int fact=1;
		  int counter=5;
		 
		  for(i=1;i<=counter;i++){    
		      fact=fact*i; 
		      
		      System.out.print(i+ "*" );    
		  }    
		  System.out.println(fact);
		      
		 } 
}
